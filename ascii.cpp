#include <iostream>

using namespace std;

int main (void) {
    printf("%c\n", 'A');
    printf("%d\n", 65);

    printf("%c\n", 65);
    printf("%d\n", 'A');
    
    printf("\n");

    printf("%x\n", 16);
    printf("%d\n", 0x10);
    
    printf("%o\n", 8);
    printf("%d\n", 010);
    return 0;
}